
public class HelloWorld {
	public static void main(String[] args) { // main + Ctrl + Space
		/* Ctrl + F11 : 실행
		 syso + Strl + Space
		 */
		System.out.println("Hello World"); // 줄변경 포함 출력
		System.out.printf("%d\n",10); // \n : 줄변경
		System.out.print("안녕"); // 줄변경 없는 출력
		System.out.println(); // 줄변경
	}
}
